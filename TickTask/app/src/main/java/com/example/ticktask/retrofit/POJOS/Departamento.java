package com.example.ticktask.retrofit.POJOS;


public class Departamento {

    String pk;

    String nombre;

    public Departamento() {
    }

    public void setPk(String pk) {
        this.pk = pk;
    }
    public String getPk() {
        return pk;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public String getNombre() {
        return nombre;
    }
}
